# Create a class called Calculator
class Calculator:

    # add
    def Add(self, num1, num2):
        return num1 + num2

    # subtract
    def Subtract(self, num1, num2):
        return num1 - num2

    # multiply
    def Multiply(self, num1, num2):
        return num1 * num2

    # divide
    def Divide(self, num1, num2):
        return num1 / num2



